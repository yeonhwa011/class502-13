package exam04;

import static java.lang.Math.*;

public class Ex01 {
    public static void main(String[] args) {
        System.out.println(abs(-10));     //절대값
        System.out.println(ceil(10.2));  //11 //올림
        System.out.println(floor(10.2)); //버림
        System.out.println(round(10.5));//반올림
        System.out.println(pow(2,4)); //2의4승
        System.out.println(sqrt(9));
    }
}
